export const translateIngredients = (ingredientsArray) => {
  if (!ingredientsArray || ingredientsArray.length === 0) {
    return "";
  }

  const translated = ingredientsArray.map((ingredient) => {
    const translation = ingredientTranslations[ingredient.toLowerCase()];
    return translation || ingredient;
  });

  return translated.join(",");
};
